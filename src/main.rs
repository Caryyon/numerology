fn convert_name(letter: &str) {
        match letter {
            "A" | "J" | "S" => 1,
            "B" | "K" | "T" => 2,
            "C" | "L" | "U" => 3,
            "D" | "M" | "V" => 4,
            "E" | "N" | "W" => 5,
            "F" | "O" | "X" => 6,
            "G" | "P" | "Y" => 7,
            "H" | "Q" | "Z" => 8,
            "I" | "R" => 9,
            &_ => 0 
        };
}

fn main() {
    convert_name("A");
}

#[test]
fn convert_name_test() {
    assert_eq!("A", convert_name("A"));
}
